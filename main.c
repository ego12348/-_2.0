#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include <windows.h>

int main() {

    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);

    double x, y;

    printf("=== Лабораторна робота №2. Варіант 12 ===\n");
    printf("Введіть значення аргументу x: ");

    if (scanf("%lf", &x) != 1) {
        printf("Помилка: введіть числове значення.\n");
        return 1;
    }


    if (x >= 0 && x < 4) {
        // 1-ша гілка: (2x-1)/(3x+1)
        y = (2 * x - 1) / (3 * x + 1);
        printf("Виконано розрахунок за 1-ю формулою (0 <= x < 4)\n");
    }
    else if (x >= 4 && x < 12) {

        y = 5 * x + 10;
        printf("Виконано розрахунок за 2-ю формулою (4 <= x < 12)\n");
    }
    else if (x >= 15 && x < 55) {

        y = (2 * x) / sqrt(-x + 2 * pow(x, 2)) - 3 * pow(x, 2);
        printf("Виконано розрахунок за 3-ю формулою (15 <= x < 55)\n");
    }
    else {

        y = 2 - (x / 10.0);
        printf("Виконано розрахунок за 4-ю формулою (інші значення x)\n");
    }

    printf("----------------------------------------\n");
    printf("Результат: y = %.4f\n", y);
    printf("----------------------------------------\n");

    return 0;
}
